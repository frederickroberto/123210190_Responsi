    private void tampilkan_data(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("ID");
        model.addColumn("Judul");
        model.addColumn("Genre");
        model.addColumn("Penulis");
        model.addColumn("Penerbit");
        model.addColumn("Lokasi");
        model.addColumn("Stock");
        try{
            int no = 1;
            int id = 1;
            String sql = "SELECT * FROM dataperpus";
            java.sql.Connection conn = Koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getInt(7)});
                
            }tabelPerpustakaan.setModel(model);
             
        }catch(SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
        
    }
