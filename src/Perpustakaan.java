    private void tabelPerpustakaanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPerpustakaanMouseClicked
        // TODO add your handling code here:
        int baris = tabelPerpustakaan.rowAtPoint(evt.getPoint());
        
        String id = tabelPerpustakaan.getValueAt(baris, 1).toString();
        txtID.setText(id);
        
        String judul = tabelPerpustakaan.getValueAt(baris, 2).toString();
        txtJudul.setText(judul);
        
        String genre = tabelPerpustakaan.getValueAt(baris, 3).toString();
        txtGenre.setText(genre);
        
        String penulis = tabelPerpustakaan.getValueAt(baris, 4).toString();
        txtPenulis.setText(penulis);
        
        String penerbit = tabelPerpustakaan.getValueAt(baris, 5).toString();
        txtPenerbit.setText(penerbit);
        
        String lokasi = tabelPerpustakaan.getValueAt(baris, 6).toString();
        txtLokasi.setText(lokasi);
        
        String stock = tabelPerpustakaan.getValueAt(baris, 7).toString();
        txtStock.setText(stock);
    }//GEN-LAST:event_tabelPerpustakaanMouseClicked

